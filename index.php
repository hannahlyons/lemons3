<script>
  function eyeball(event) {
    var eye = document.querySelectorAll(".eye");
    eye.forEach(function (eye) {
      let x = (eye.getBoundingClientRect().left) + (eye.clientWidth / 50);
      let y = (eye.getBoundingClientRect().top) + (eye.clientHeight / 50);

      // Adjust for viewport size
      let viewportWidth = window.innerWidth;
      let viewportHeight = window.innerHeight;
      x = x + (viewportWidth / 2 - event.pageX) / viewportWidth * 100;
      y = y + (viewportHeight / 2 - event.pageY) / viewportHeight * 100;

      let radian = Math.atan2(event.pageX - x, event.pageY - y);
      let rot = (radian * (180 / Math.PI) * -1) + 270;
      eye.style.transform = "rotate(" + rot + "deg)";
    });
  }

  document.querySelector("footer").addEventListener("mousemove", eyeball);
</script>
<script>
  function isElementInViewport(el) {
    var rect = el.getBoundingClientRect();
    return (
      rect.top >= 0 &&
      rect.left >= 0 &&
      rect.bottom <= (window.innerHeight || document.documentElement.clientHeight) &&
      rect.right <= (window.innerWidth || document.documentElement.clientWidth)
    );
  }

  var animatedH1 = document.querySelector('.animated-h1');
  window.addEventListener('scroll', function () {
    if (isElementInViewport(animatedH1)) {
      animatedH1.classList.add('animated');
    }
  });
</script>
